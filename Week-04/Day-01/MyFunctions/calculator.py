def add(x,y):
    """
    This function adds two values and return addition result
    - You have to provide values for x and y
    """
    return x + y

def sub(x,y):
    """
    This function substract two values and return Substraction result
    - You have to provide values for x and y
    """
    return x - y

def multiply(x,y):
    """
    This function multiple two values and return multiplication result
    - You have to provide values for x and y
    """
    return x * y

